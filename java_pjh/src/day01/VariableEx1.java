package day01;

/*변수 선언 방법
 * 1. 변수를 선언만 함
 * 변수타입 변수명;
 * 변수를 선언하고 초기화 함
 * 변수타입 변수명 = 값;
 */

public class VariableEx1 {

	public static void main(String[] args) {
		char ch1;
		ch1 = '+';
		char ch2 = 'A';
		char ch5 = '\u0065'; // 유니코드
		char ch6 = 65; // 아스키코드
		char ch7 = '\n';
		System.out.println(ch2);
		System.out.println(ch1);
		System.out.println(ch5);
		System.out.println(ch7);
		System.out.println(ch6);
		
		
		
	}
}
