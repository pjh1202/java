package day13;

public class MathEx1 {

	public static void main(String[] args) {
		// Math 클래스 예제
		
		//소수점 첫째자리에서 올림
		System.out.println(Math.ceil(123.4567));
		
		//내림
		System.out.println(Math.floor(123.4567));
		
		//반올림
		System.out.println(Math.round(123.4567));
		
		//절댓값
		System.out.println(Math.abs(-1));
		
		//2의 3제곱
		System.out.println(Math.pow(2, 3));
		
		//121의 제곱근
		System.out.println(Math.sqrt(121));
	}
}
