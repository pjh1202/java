package day02;

public class OperatorEx4 {
	//비교(관계) 연산자
	public static void main(String[] args) {
		
		int num1 = 1, num2 = 2;
		
		System.out.println((num1>num2));
		System.out.println((num1<num2));
		System.out.println((num1>=num2));
		System.out.println((num1<=num2));
		System.out.println((num1==num2));
		System.out.println((num1!=num2));
		
		
		
	}

}
